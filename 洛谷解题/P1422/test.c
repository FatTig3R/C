#include <stdio.h>

int main()
{
	
	int sum; //用电量总计
	double b;
	scanf("%d", &sum);
	if (sum <= 150)
		//月用电量在150千瓦时及以下部分按每千瓦时0.4463元执行
		b = sum * 0.4463;
	if  ((sum >= 151) && (sum <= 400))
		//月用电量在151~400千瓦时的部分按每千瓦时0.4663元执行
		b = 150 * 0.4463 + (sum - 150)  * 0.4663;
	if (sum >= 401)
		//月用电量在401千瓦时及以上部分按每千瓦时0.5663元执行
		b = 150*0.4463 + 250 * 0.4663 + (sum - 400) * 0.5663;
	printf("%.1f\n", b);
	
	return 0;
}